package string;

public class ReplaceDemo {
	public static void main(String[] args) {
		//replace();
		//replaceAll();
		replaceFirst();
	}

	//replace
	static void replace()
	{
		String str = "ABCabcABCabc";
		String sRes = str.replace("abc", "123");
		System.out.println(sRes);
	}
	
	//replaceAll
	static void replaceAll()
	{
		String str = "ABCabcABCabc";
		String sRes = str.replaceAll("abc", "123");
		System.out.println(sRes);
	}
	
	
	//replaceFirst
	static void replaceFirst()
	{
		String str = "ABCabcABCabc";
		String sRes = str.replaceFirst("abc", "123");
		System.out.println(sRes); 
	}
}